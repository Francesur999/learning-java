
public class Contador_Inverso {

	public static void main(String[] args) {

		int i = 1200;
		

		while (i <= 1200 && i >= 620) {
			System.out.print(i+ " ");
			i = i -20;

		}
		
		System.out.println(" ");
		
		i = 1200;
		
		do {
			System.out.print(i+ " ");
			i = i -20;
			
		}while(i <= 1200 && i >= 620);
		
		System.out.println(" ");
		
		for(int j = 1200;j <= 1200 && j >= 620; j = j - 20 )
		{
		
			System.out.print(j+ " ");

	}
}
}
